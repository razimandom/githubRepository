package com.raziman.petronas.model;

public class GitRepo {
	
	private int repoNo;
	private String repoId;
	private String repoName;
	private String repoPrivate;
	private String repoDescription;
	private String repoURL;
	private String repoOwnerId;
	private String repoHomePage;
	private String repoDateCreated;
	private String repoDateUpdated;
	private String repoDatePushed;
	private String repoSize;
	private String repoWatcherCount;
	private String repoScore;
	private String repoLanguage;
	
	public int getRepoNo() {
		return repoNo;
	}
	public void setRepoNo(int x) {
		this.repoNo = x;
	}
	public String getRepoId() {
		return repoId;
	}
	public void setRepoId(String repoId) {
		this.repoId = repoId;
	}
	public String getRepoName() {
		return repoName;
	}
	public void setRepoName(String repoName) {
		this.repoName = repoName;
	}
	public String getRepoPrivate() {
		return repoPrivate;
	}
	public void setRepoPrivate(String repoPrivate) {
		this.repoPrivate = repoPrivate;
	}
	public String getRepoDescription() {
		return repoDescription;
	}
	public void setRepoDescription(String repoDescription) {
		this.repoDescription = repoDescription;
	}
	public String getRepoURL() {
		return repoURL;
	}
	public void setRepoURL(String repoURL) {
		this.repoURL = repoURL;
	}
	public String getRepoOwnerId() {
		return repoOwnerId;
	}
	public void setRepoOwnerId(String repoOwnerId) {
		this.repoOwnerId = repoOwnerId;
	}
	public String getRepoHomePage() {
		return repoHomePage;
	}
	public void setRepoHomePage(String repoHomePage) {
		this.repoHomePage = repoHomePage;
	}
	public String getRepoDateCreated() {
		return repoDateCreated;
	}
	public void setRepoDateCreated(String repoDateCreated) {
		this.repoDateCreated = repoDateCreated;
	}
	public String getRepoDateUpdated() {
		return repoDateUpdated;
	}
	public void setRepoDateUpdated(String repoDateUpdated) {
		this.repoDateUpdated = repoDateUpdated;
	}
	public String getRepoDatePushed() {
		return repoDatePushed;
	}
	public void setRepoDatePushed(String repoDatePushed) {
		this.repoDatePushed = repoDatePushed;
	}
	public String getRepoSize() {
		return repoSize;
	}
	public void setRepoSize(String repoSize) {
		this.repoSize = repoSize;
	}
	public String getRepoWatcherCount() {
		return repoWatcherCount;
	}
	public void setRepoWatcherCount(String repoWatcherCount) {
		this.repoWatcherCount = repoWatcherCount;
	}
	public String getRepoScore() {
		return repoScore;
	}
	public void setRepoScore(String repoScore) {
		this.repoScore = repoScore;
	}
	public String getRepoLanguage() {
		return repoLanguage;
	}
	public void setRepoLanguage(String repoLanguage) {
		this.repoLanguage = repoLanguage;
	}
	
	
	
}