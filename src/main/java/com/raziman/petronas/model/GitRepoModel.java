package com.raziman.petronas.model;

public class GitRepoModel {
	
	private String repoId;
	private String repoName;
	private String repoPrivate;
	private String repoDescription;
	private String repoURL;
	private String repoOwnerId;
	
	public String getRepoId() {
		return repoId;
	}
	public void setRepoId(String repoId) {
		this.repoId = repoId;
	}
	public String getRepoName() {
		return repoName;
	}
	public void setRepoName(String repoName) {
		this.repoName = repoName;
	}
	public String getRepoPrivate() {
		return repoPrivate;
	}
	public void setRepoPrivate(String repoPrivate) {
		this.repoPrivate = repoPrivate;
	}
	public String getRepoDescription() {
		return repoDescription;
	}
	public void setRepoDescription(String repoDescription) {
		this.repoDescription = repoDescription;
	}
	public String getRepoURL() {
		return repoURL;
	}
	public void setRepoURL(String repoURL) {
		this.repoURL = repoURL;
	}
	public String getRepoOwnerId() {
		return repoOwnerId;
	}
	public void setRepoOwnerId(String repoOwnerId) {
		this.repoOwnerId = repoOwnerId;
	}
	
	
	
}